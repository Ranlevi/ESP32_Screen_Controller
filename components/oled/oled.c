// ---------------------------------------------------------------------------
// Framebuffer
// 128 pixels wide, 64 pixels tall, 1 bit per pixel = 1024 bytes.
// Laid out in SSD1306 page order: 8 pages of 128 bytes, one byte per column,
// LSB = topmost pixel in page.
//
// The framebuffer concept:
// A copy of the display in RAM. You write to the buffer and then call flush()
// to push the data over I2C.
// The reason is that you need to send the whole display even if you change 
// only one character. The buffer allows you to compose multiple changes and
// send them all in one flush() call. 
// ---------------------------------------------------------------------------

#define FB_SIZE     (OLED_PIXEL_WIDTH * OLED_PIXEL_HEIGHT / 8)

static uint8_t s_framebuffer[FB_SIZE];

// ---------------------------------------------------------------------------
// Module state
// ---------------------------------------------------------------------------

static const char *TAG = "oled";

static esp_lcd_panel_handle_t s_panel       = NULL;
static bool                   s_initialized = false;

// ---------------------------------------------------------------------------
// Internal helpers
// ---------------------------------------------------------------------------

//  Writes s_framebuffer to the display.
static esp_err_t flush(void)
{
    //  draw_bitmap expects: x_start, y_start, x_end (exclusive), y_end (exclusive), data
    return esp_lcd_panel_draw_bitmap(
        s_panel,
        0, 0,
        OLED_PIXEL_WIDTH, OLED_PIXEL_HEIGHT,
        s_framebuffer);
}

//  Blits one character glyph into s_framebuffer at pixel position (x, y).
//  y must be page-aligned (multiple of 8) for correct rendering.
static void blit_char(int x, int y, char c)
{
    if (c < FONT_FIRST_CHAR || c > FONT_LAST_CHAR) {
        c = ' ';
    }

    const uint8_t *glyph = s_font8x8[(uint8_t)(c - FONT_FIRST_CHAR)];
    int page = y / FONT_CHAR_HEIGHT;

    for (int col = 0; col < FONT_CHAR_WIDTH; col++) {
        int px = x + col;
        if (px >= OLED_PIXEL_WIDTH) {
            break;
        }
        //  The font is row-major: glyph[row] has one bit per column, LSB = left.
        //  The SSD1306 framebuffer is column-major: each byte is a vertical strip
        //  of 8 pixels within a page, LSB = top pixel.
        //  Transpose: for each output column, collect one bit from each font row.
        uint8_t col_byte = 0;
        for (int row = 0; row < FONT_CHAR_HEIGHT; row++) {
            if (glyph[row] & (1u << col)) {
                col_byte |= (uint8_t)(1u << row);
            }
        }
        s_framebuffer[page * OLED_PIXEL_WIDTH + px] = col_byte;
    }
}

