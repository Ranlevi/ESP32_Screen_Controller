/*
    Oled screen abstraction explained:
    There are three layers, each built on top of the previous.
    - bus_handle: lowest layer - raw I2C bus, created by i2c_new_master_bus().
      Sends bytes over I2C.
    - io_handle: created by esp_lcd_new_panel_io_i2c(). Display-oriented I2C
      session: has the device address, and handles the framing esp_lcd expects 
      (command vs. data mode)
    - s_panel: created by esp_lcd_new_panel_ssd1306. The SSD1306 driver itself: 
      understands the SSD1306 commands - how to reset, init, etc. Exposes the 
      esp_lcd_panel_* API (reset, init, draw_bitmap, etc.) that the app actually
      calls.
*/
esp_err_t oled_init(void)
{
    //Prevents calling the function twice.
    if (s_initialized) {
        return ESP_OK;
    }

    // --- I2C master bus ---
    i2c_master_bus_config_t bus_cfg = {
        .i2c_port      = OLED_I2C_PORT,
        .sda_io_num    = OLED_I2C_SDA_GPIO,
        .scl_io_num    = OLED_I2C_SCL_GPIO,
        .clk_source    = I2C_CLK_SRC_DEFAULT,
        .glitch_ignore_cnt = 7,
        .flags.enable_internal_pullup = true,
    };

    i2c_master_bus_handle_t bus_handle;
    esp_err_t err = i2c_new_master_bus(&bus_cfg, &bus_handle);
    if (err != ESP_OK) {
        ESP_LOGE(TAG, "i2c_master_bus_create failed: %s", esp_err_to_name(err));
        return err;
    }

    // --- LCD panel IO (I2C transport) ---
    esp_lcd_panel_io_handle_t io_handle;
    esp_lcd_panel_io_i2c_config_t io_cfg = {
        .dev_addr            = OLED_I2C_ADDR,
        .scl_speed_hz        = OLED_I2C_FREQ_HZ,
        .control_phase_bytes = 1,
        .dc_bit_offset       = 6,
        .lcd_cmd_bits        = 8,
        .lcd_param_bits      = 8,
    };

    //Create LCD panel IO handle
    err = esp_lcd_new_panel_io_i2c(bus_handle, &io_cfg, &io_handle);
    if (err != ESP_OK) {
        ESP_LOGE(TAG, "esp_lcd_new_panel_io_i2c failed: %s", esp_err_to_name(err));
        return err;
    }

    // --- SSD1306 panel driver ---
    esp_lcd_panel_dev_config_t panel_cfg = {
        .bits_per_pixel = 1,
        .reset_gpio_num = -1,   // No hard-reset GPIO wired on this board
    };

    err = esp_lcd_new_panel_ssd1306(io_handle, &panel_cfg, &s_panel);
    if (err != ESP_OK) {
        ESP_LOGE(TAG, "esp_lcd_new_panel_ssd1306 failed: %s", esp_err_to_name(err));
        return err;
    }

    err = esp_lcd_panel_reset(s_panel);
    if (err != ESP_OK) {
        ESP_LOGE(TAG, "esp_lcd_panel_reset failed: %s", esp_err_to_name(err));
        return err;
    }

    err = esp_lcd_panel_init(s_panel);
    if (err != ESP_OK) {
        ESP_LOGE(TAG, "esp_lcd_panel_init failed: %s", esp_err_to_name(err));
        return err;
    }

    err = esp_lcd_panel_disp_on_off(s_panel, true);
    if (err != ESP_OK) {
        ESP_LOGE(TAG, "esp_lcd_panel_disp_on_off failed: %s", esp_err_to_name(err));
        return err;
    }

    memset(s_framebuffer, 0, sizeof(s_framebuffer));

    s_initialized = true;
    ESP_LOGI(TAG, "SSD1306 initialized (%dx%d)", OLED_PIXEL_WIDTH, OLED_PIXEL_HEIGHT);
    return ESP_OK;
}

esp_err_t oled_clear(void)
{
    if (!s_initialized) {
        return ESP_ERR_INVALID_STATE;
    }

    memset(s_framebuffer, 0, sizeof(s_framebuffer));
    return flush();
}

esp_err_t oled_show_text(int col, int row, const char *text)
{
    if (!s_initialized) {
        return ESP_ERR_INVALID_STATE;
    }

    if (text == NULL) {
        return ESP_ERR_INVALID_ARG;
    }

    //  row must be page-aligned; silently round down.
    int y = (row / FONT_CHAR_HEIGHT) * FONT_CHAR_HEIGHT;

    int x = col;
    for (const char *p = text; *p != '\0'; p++) {
        if (x + FONT_CHAR_WIDTH > OLED_PIXEL_WIDTH) {
            break;  // clip — don't wrap
        }
        blit_char(x, y, *p); //Insert text into the framebuffer.
        x += FONT_CHAR_WIDTH;
    }

    return flush(); //actual writing to the display.
}
